/**
 * @private
 * @param {number[]} a
 * @returns {number}
 */
function sum(a) {
    let res = 0;
    a.forEach((v) => (res += v));
    return res;
}

function extent(arr) {
    let min = Infinity;
    let max = -Infinity;
    arr.forEach((value) => {
        if (value > max) max = value;
        if (value < min) min = value;
    });
    return [min, max];
}

function makeGrid(data, div) {
    const N = data.features.length;
    let center = [sum(data.features.map((d) => d.geometry.coordinates[0])) / N, sum(data.features.map((d) => d.geometry.coordinates[1])) / N];
    const [min_lon, max_lon] = extent(data.features.map((d) => d.geometry.coordinates[0]));
    const [min_lat, max_lat] = extent(data.features.map((d) => d.geometry.coordinates[1]));

    const span_lon = max_lon - min_lon;
    const span_lat = max_lat - min_lat;

    const max_span = Math.max(span_lat, span_lon);

    const step_size = max_span / div;
    const result = Array.from({ length: div }, (d, i) => {
        return Array.from({ length: div }, (_, j) => {
            return [center[0] - max_span / 2 + i * step_size, center[1] - max_span / 2 + j * step_size];
        });
    });
    result.step_size = step_size;
    return result;
}

/**
 * Cividis colorscale
 * @param {number} t - value between 0 and 1
 * @returns {String} the color.
 */
function cividis(t) {
    t = Math.max(0, Math.min(1, t));
    return "rgb(" + Math.max(0, Math.min(255, Math.round(-4.54 - t * (35.34 - t * (2381.73 - t * (6402.7 - t * (7024.72 - t * 2710.57))))))) + ", " + Math.max(0, Math.min(255, Math.round(32.49 + t * (170.73 + t * (52.82 - t * (131.46 - t * (176.58 - t * 67.37))))))) + ", " + Math.max(0, Math.min(255, Math.round(81.24 + t * (442.36 - t * (2482.43 - t * (6167.24 - t * (6614.94 - t * 2475.67))))))) + ")";
}

/**
 * @private
 * Colorscale
 * @param {number} t - value between 0 and 1
 * @returns {Function} the color.
 */
function ramp(range) {
    var n = range.length;
    return function (t) {
        return range[Math.max(0, Math.min(n - 1, Math.floor(t * n)))];
    };
}

/**
 * @private
 */
function colors(specifier) {
    var n = (specifier.length / 6) | 0,
        colors = new Array(n),
        i = 0;
    while (i < n) colors[i] = "#" + specifier.slice(i * 6, ++i * 6);
    return colors;
}

