"use strict";

let metaInfo;
let main;

let selection_property;
let property = "zinc"

// width and height of drawing area
const drawingAreaSize = {
  width: 900,
  height: 900,
};

function initPage() {
    //populate page with required elements, e.g., header for title and drawing area
    main = document.getElementById("main");
    metaInfo = getTutorialInfo(); //should be provided in app.js
    if (!metaInfo) metaInfo = { groupNames: "ERROR", exerciseNum: "ERROR" };

    const mainTitle = document.createElement("h1");
    mainTitle.style.fontFamily = "sans-serif";
    mainTitle.style.margin = "1rem auto";
    mainTitle.style.textAlign = "center";
    mainTitle.textContent = "InfoVIS Exercise " + metaInfo.exerciseNum;
    main.appendChild(mainTitle);

    document.title = "InfoVIS Exercise " + metaInfo.exerciseNum;

    const groupBlock = document.createElement("h2");
    groupBlock.style.fontFamily = "sans-serif";
    groupBlock.style.margin = "1rem auto";
    groupBlock.style.textAlign = "center";
    groupBlock.style.color = "gray";
    groupBlock.textContent = metaInfo.groupNames;
    main.appendChild(groupBlock);

    const controls_container = document.createElement("div");
    controls_container.style.fontFamily = "sans-serif";
    controls_container.style.margin = "1rem auto";
    controls_container.style.textAlign = "center";

    const selection_label = document.createElement("label");
    selection_label.textContent = "Select Property: "
    selection_property = document.createElement("select");
    for (const prop of ["cadmium", "copper", "lead", "zinc", "elev", "dist", "om", "ffreq", "soil", "lime", "dist.m"]) {
        const selection_option = document.createElement("option");
        selection_option.value = prop;
        selection_option.text = prop;
        selection_property.appendChild(selection_option);
    };
    selection_property.value = property

    selection_label.appendChild(selection_property)
    controls_container.appendChild(selection_label)
    main.appendChild(controls_container)

    const { width, height } = drawingAreaSize;

    const drawingArea = document.createElement("div");
    drawingArea.id = "mainCanvas";
    drawingArea.style.width = `${width}px`;
    drawingArea.style.height = `${height}px`;
    drawingArea.style.margin = "2rem auto";
    main.appendChild(drawingArea);

    return drawingArea;
}

function start() {
    if (document.getElementById("mainCanvas")) return; //already initialized

    const drawingArea = initPage();

    const { width, height } = drawingAreaSize;

    const two = new Two({ width, height, type: Two.Types.canvas });
    two.appendTo(drawingArea);

    selection_property.addEventListener("input", (event) => {
        property = selection_property.value;
        two.clear()
        draw(two, width, height)
        two.update();
    })

    //perform drawing instructions, this is defined in app.js
    draw(two, width, height)

    two.update();
}

